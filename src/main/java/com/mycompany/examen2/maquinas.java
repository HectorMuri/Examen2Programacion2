    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:
        int res = JOptionPane.showOptionDialog(new JFrame(), "¿Esta seguro que desea actualizar el registro seleccionado?", 
                    "Confirmacion de actualizacion",
                    JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null,
                    new Object[] { "Si", "No" }, JOptionPane.YES_OPTION);

        int posicion = this.tblMaquinarias.getSelectedRow();
        if(posicion>=0 && res==JOptionPane.YES_OPTION ){
           int nCol = this.modelMaquinas.getColumnCount();
           String[] dataTabla = new String[nCol];
           for(int i=0;i<nCol;i++){
            dataTabla[i]=this.modelMaquinas.getValueAt(posicion, i).toString();
           }

           Document datosObj;
            datosObj = new Document("ID Maquinaria",this.txtID.getText())
                    .append("Marca",this.txtMarca.getText())
                    .append("Modelo",this.txtModelo.getText())
                    .append("VidaUtil",this.txtVida.getText());
           
           JOptionPane.showMessageDialog(null, main.connM.actualizarDocuments(this.Maquinas,datosObj,dataTabla[0])?"Registro Actualizado con exito":"Registro no pudo ser actualizado");
          
           this.modelMaquinas.setValueAt(this.txtID.getText(), posicion, 1);
           this.modelMaquinas.setValueAt(this.txtMarca.getText(), posicion, 2);
           this.modelMaquinas.setValueAt(this.txtModelo.getText(), posicion, 3);
           this.modelMaquinas.setValueAt(this.txtVida.getText(), posicion, 4);
           this.limpiarForm();
           this.tblMaquinarias.clearSelection();
      
        }else{
           JOptionPane.showMessageDialog(null, "Seleccione un registro de la tabla");
        }

    }//GEN-LAST:event_btnActualizarActionPerformed

    private void tblMaquinariasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMaquinariasMouseClicked
        // TODO add your handling code here:
        int posicion = this.tblMaquinarias.getSelectedRow();
        if(posicion>=0){
           int nCol = this.modelMaquinas.getColumnCount();
           String[] dataTabla = new String[nCol];
           for(int i=0;i<nCol;i++){
            dataTabla[i]=this.modelMaquinas.getValueAt(posicion, i).toString();
           }

           this.txtID.setText(dataTabla[1]);
           this.txtMarca.setText(dataTabla[2]);
           this.txtModelo.setText(dataTabla[3]);
           this.txtVida.setText(dataTabla[4]);
        }else{
           JOptionPane.showMessageDialog(null, "Seleccione otro registro de la tabla");
        }
    }//GEN-LAST:event_tblMaquinariasMouseClicked

