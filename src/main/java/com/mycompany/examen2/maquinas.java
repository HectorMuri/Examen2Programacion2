public boolean deleteTablePersonas(){
     int posicion = this.tblMaquinarias.getSelectedRow();
     if(posicion>=0){
        String id=this.modelMaquinas.getValueAt(posicion, 0).toString();     
        this.modelMaquinas.removeRow(posicion);
        main.connM.deleteDocuments(this.Maquinas,id);
        return true;
     }else{
        return false;
     }
}
